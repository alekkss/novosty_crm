/**
 * –û–±–Ω–æ–≤–∏—Ç—å —Å–æ—Å—Ç–æ—è–Ω–∏–µ –∫–Ω–æ–ø–æ–∫ —Ñ–∏–ª—å—Ç—Ä–∞
 * @param {number} activeIndex - –∏–Ω–¥–µ–∫—Å –∞–∫—Ç–∏–≤–Ω–æ–π –∫–Ω–æ–ø–∫–∏ (0 –∏–ª–∏ 1)
 */
function updateFilterButtons(activeIndex) {
    const filterButtons = document.querySelectorAll('.filter-button');
    filterButtons.forEach((btn, index) => {
        btn.classList.remove('active');
        if (index === activeIndex) {
            btn.classList.add('active');
        }
    });
}

/**
 * –û–±–Ω–æ–≤–∏—Ç—å –∑–∞–≥–æ–ª–æ–≤–æ–∫ –∫–æ–Ω—Ç–µ–Ω—Ç–∞
 * @param {string} title - –Ω–æ–≤—ã–π –∑–∞–≥–æ–ª–æ–≤–æ–∫
 */
function updateContentTitle(title) {
    const titleElement = document.getElementById('contentTitle');
    if (titleElement) {
        titleElement.textContent = title;
    }
}

